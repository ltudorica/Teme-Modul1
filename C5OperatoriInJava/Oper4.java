package C5OperatoriInJava;
public class Oper4 {
	public static void main(String[] args) {
		
		int pleaseNegateMe = 100;
		
		System.out.println("1. " + (-pleaseNegateMe));
		System.out.println("2. " + (pleaseNegateMe *= -1));
		
		pleaseNegateMe = 100;
		System.out.println("3. " + (pleaseNegateMe /= -1));
		
		
	}
}
